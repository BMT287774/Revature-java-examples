package operators;

public class multiplicationanddivision {
	public static void main(String[] args) {
		int x = 5;
		int y = 3;
		int z = x*x;
		int w = z*y;
		int v = w/x;
		System.out.println("z is : "+z);
		System.out.println("w is : "+w);
		System.out.println("v is: "+v);
		
	}
	

}
