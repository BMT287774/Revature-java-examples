package relational;

public class relationalthings {
	public static void main(String[] args) {
		int a, b;
		a = 7;
		b = 8;
		System.out.println(a==b);
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a!=b);
	}

}
